'''
题目背景
改编自 NOIP 2004 普及组 T1

题目描述
小鲸鱼上初中了。鲸鱼妈妈认为小鲸鱼应该更加用功学习，所以小鲸鱼除了上学之外，还要参加鲸鱼妈妈为它报名的各科复习班。另外每周鲸鱼妈妈还会送它去学习朗诵、舞蹈和钢琴。但是小鲸鱼如果一天上课超过八个小时就会不高兴，而且上得越久就会越不高兴。假设小鲸鱼不会因为其它事不高兴，并且它的不高兴不会持续到第二天。请你帮忙检查一下小鲸鱼下周的日程安排，看看下周它会不会不高兴；如果会的话，哪天最不高兴。

输入格式
输入包括 7 行数据，分别表示周一到周日的日程安排。每行包括两个小于10 的非负整数，用空格隔开，分别表示小鲸鱼在学校上课的时间和鲸鱼妈妈安排它上课的时间。

输出格式
一个数字。如果不会不高兴则输出0，如果会则输出最不高兴的是周几（用1,2,3,4,5,6,7 分别表示周一，周二，周三，周四，周五，周六，周日）。如果有两天或两天以上不高兴的程度相当，则输出时间最靠前的一天。

输入数据 1
5 3
6 2
7 2
5 3
5 4
0 4
0 6
输出数据 1
3
'''

max_over = -1
result_day = 0

for i in range(1, 8):
    a, b = map(int, input().split())
    total = a + b
    if total > 8:
        over = total - 8
        if over > max_over:
            max_over = over
            result_day = i

print(result_day if result_day != 0 else 0)